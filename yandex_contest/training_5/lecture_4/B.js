// B. Одномерный морской бой

// Ограничение времени 2 секунды
// Ограничение памяти 256Mb
// Ввод стандартный ввод или input.txt
// Вывод стандартный вывод или output.txt

// Поле в игре в одномерный морской бой имеет размеры 1×n.
// Ваша задача — найти такое максимальное k, что на поле можно расставить один корабль размера 1×k,
// два корабля размера 1×(k−1), …, k кораблей размера 1×1, причем корабли, как и в обычном морском бое,
// не должны касаться друг друга и пересекаться.

// Формат ввода
// В единственной строке входных данных дано число n — количество клеток поля (0≤n≤10**18).

// Формат вывода
// Выведите единственное число — такое максимальное k, что можно расставить корабли, как описано в условии.

// Пример
// Ввод     Вывод
// 7        2

// Примечания
// Пояснение к примеру: для поля 1×7 ответ равен 2.
// Расставить один корабль размера 1×2 и два корабля размера 1×1

const readline = require('readline');
const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

rl.on('line', (line) => {
  const N = BigInt(line);
  let l = BigInt(0);
  let r = N + BigInt(1);

  while (r - l > BigInt(1)) {
    const m = (l + r) / BigInt(2);
    const doubleM = BigInt(2) * m;
    const incM = m + BigInt(1);
    const ships = m * incM * (doubleM + BigInt(1)) / BigInt(6);
    const spaces = m * m * incM / BigInt(2);

    if (spaces - ships + m * incM <= N + BigInt(1)) {
      l = m;
    } else {
      r = m;
    }
  }

  console.log(l.toString());
  rl.close();
});

